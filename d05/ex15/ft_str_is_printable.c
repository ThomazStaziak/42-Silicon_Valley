int	ft_str_is_printable(char *str)
{
	int i;

	i = 0;
	if (!*str)
		return (1);
	while ((str[i] >= 32 && str[i] <= 126))
	{
		if (str[i + 1] == '\0')
			return (1);
		i++;
	}
	return (0);
}
